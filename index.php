						<h3><span>What Does Hidaya Mean?</span></h3>
						<p>
One of the beautiful names of the Creator is Al-Hadi (The Guide), and Hidaya (Guidance) is one of the most important themes of the Holy Qur’an. Hidaya means “To lead and to guide.” From it also comes the word Hadiyyah, which means, “present or gift.” Hidaya, or Guidance, is the gift of the Creator to His creation.
 
According to Imam Raghib Al-Isphani, the famous lexicographer of the Holy Qur’an, Hidaya means “showing the way or leading to the goal in a gentle and kind way.” It means both explanation and direction; and it also means helping and supporting a person to reach to his goal						</p>
